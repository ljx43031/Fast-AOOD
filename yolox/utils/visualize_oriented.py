import cv2
import numpy as np

__all__ = ["vis_oriented"]


def vis_oriented(img, boxes, scores, cls_ids, rotates, conf=0.5, class_names=None):

    for i in range(len(boxes)):
        box = boxes[i]
        cls_id = int(cls_ids[i])
        score = scores[i]
        if score < conf:
            continue

        color = (_COLORS[cls_id] * 255).astype(np.uint8).tolist()
        text = '{}:{:.1f}%'.format(class_names[cls_id], score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX

        txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]

        center_x = int((box[2] + box[0]) / 2)
        center_y = int((box[3] + box[1]) / 2)
        w = float(box[2] - box[0])
        h = float(box[3] - box[1])

        rotate = float(rotates[i])
        Plus90 = 0
        if rotate > 90:
            Plus90 = 1
            temp = h
            h = w
            w = temp
        center = (center_x, center_y)
        rect_min = (center, (w,h),(rotate-Plus90*90))
        bbox = cv2.boxPoints(rect_min)
        bbox = bbox.astype(int)
        img = cv2.drawContours(img, [bbox], 0, color, 2)
        txt_bk_color = (_COLORS[cls_id] * 255 * 0.7).astype(np.uint8).tolist()
        cv2.rectangle(
            img,
            (center_x, center_y + 1),
            (center_x + txt_size[0] + 1, center_y + int(1.5*txt_size[1])),
            txt_bk_color,
            -1
        )
        cv2.putText(img, text, (center_x, center_y + txt_size[1]), font, 0.4, txt_color, thickness=1)

    return img


